#!/usr/bin/env python3
"""
module that annotate function add that takes
a float a and a float b as arguments and returns
their sum as a float.
"""


def add(a: float, b: float) -> float:
    """add the sum of two float number

   Args:
   a (float) -- first float argument
   b (float) -- seconf float number argument

   Returns:
    floats: return the sum of both float numbers
   """
    return a + b
