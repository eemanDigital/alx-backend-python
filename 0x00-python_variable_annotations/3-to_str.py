#!/usr/bin/env python3
"""
Module that execute a type-annotated function
to_str that takes a float n as argument and
returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    function that return string
    representation of a float number

    Arg:
    n (float): input
    Return:
    string: the string to be return as rep of float
    """
    return str(n)
